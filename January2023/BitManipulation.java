/**
 * BitManipulation
 */
public class BitManipulation {

    public static void main(String[] args) {
        System.out.println(5 & 6); // Binary & Operator
        System.out.println(5 | 6); // Binary OR Operator
        System.out.println(5 ^6); // Binary XOR Operator
        System.out.println(~5); // Binary One's Complement
        System.out.println(~0);
        System.out.println(5<<3); // Binary Left Shift
        System.out.println(16>>3); // Binary Right Shift
    }

}