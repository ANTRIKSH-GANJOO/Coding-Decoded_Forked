public class Operators {
 public static void main(String[] args) {
    int a = 20;
    int b = 10;

    System.out.println(a+b);
    System.out.println(a-b);
    System.out.println(a*b);
    System.out.println(a/b);

    int c = 10;
    int d = ++c;
    System.out.println(c);
    System.out.println(d);
    int e = 10;
    int f = e++;
    System.out.println(e);
    System.out.println(f);


 }   
}
